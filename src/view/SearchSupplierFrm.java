    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (inpName.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập tư khóa!");
        } else {
            SupplierDAO sd = new SupplierDAO();
            listSupplier = sd.searchSuppliers(inpName.getText().trim());
            String[][] val = new String[listSupplier.size()][4];
            for (int i = 0; i < listSupplier.size(); ++i) {
                val[i][0] = String.valueOf(listSupplier.get(i).getId());
                val[i][1] = listSupplier.get(i).getName();
                val[i][2] = listSupplier.get(i).getPhone_number();
                val[i][3] = listSupplier.get(i).getAddress();
            }
            String[] cols = {"ID", "Tên nhà cung cấp", "SĐT", "Địa chỉ"};
            md = new DefaultTableModel(val, cols){
                public boolean isCellEditable(int row,int col){
                    return false;
                }
            };
            tblSupplier.setModel(md);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

