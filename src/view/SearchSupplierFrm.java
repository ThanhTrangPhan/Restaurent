    private void tblSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSupplierMouseClicked
        // TODO add your handling code here:
        int col=tblSupplier.getColumnModel().getColumnIndexAtX(evt.getX());
        int row=evt.getY()/tblSupplier.getRowHeight();
        Invoice inv = new Invoice();
        inv.setStaff(s);
        inv.setSupplier(listSupplier.get(row));
        if(row<tblSupplier.getRowCount()&&row>=0&& col<tblSupplier.getColumnCount()&& col>=0){
            (new SearchIngredientFrm(s,listSupplier.get(row),new ArrayList<DetailStock_Ingredient>())).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_tblSupplierMouseClicked

